# ‚ïî‚ïê‚ï° 7289692b-1a85-4a84-b7cc-fea1e46c9f31
function htrace(img;
	zmin = 0,
	zmax = zmax,
	title = "ADU",
	restrict = true,
)
	if restrict
		img_small = r2(img)
	else
		img_small = img
	end
	
	img_small = permutedims(img_small)
	
	heatmap(;
		x = img_small.dims[1].val,
		y = img_small.dims[2].val,
		z = img_small.data,
		zmin,
		zmax,
		colorbar = attr(; title),
		colorscale = :Cividis,
	)
end

# ‚ïî‚ïê‚ï° 2ba90b91-5de2-44a2-954f-a73b1561e762
function plot_img(i, img; restrict=true)
	hm = htrace(img; restrict)
	
	l = Layout(
		width = size(img, 1) / 2,
	    height = size(img, 2) / 2,
		title = string("Frame $(i): ", timestamp(img)),
		xaxis = attr(title="X", constrain=:domain),
		yaxis = attr(title="Y", scaleanchor=:x, constrain=:domain),
		uirevision = 1,
	)

	plot(hm, l)
end

# ‚ïî‚ïê‚ï° 84745bd9-c2b1-45c3-8376-7f18d600e7eb
function circ(ap; line_color=:lightgreen)
	circle(
		ap.x - ap.r/2, # x_min
		ap.x + ap.r/2, # x_max
		ap.y - ap.r/2, # y_min
		ap.y + ap.r/2; # y_max
		line_color,
	)
end

